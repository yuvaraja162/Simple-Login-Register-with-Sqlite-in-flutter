import 'package:flutter/foundation.dart';
import 'package:sqflite/sqflite.dart' as sql;

class SQLHelper {
  static Future<void> createTables(sql.Database db) async {
    await db.execute("""CREATE TABLE emp_users(
        id INTEGER PRIMARY KEY AUTOINCREMENT NOT NULL,
        emp_username TEXT,
        emp_password TEXT,
        emp_email TEXT,
        emp_phone TEXT,
        createdAt TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
      )
      """);
  }
// id: the id of a item
// title, description: name and description of your activity
// created_at: the time that the item was created. It will be automatically handled by SQLite

  static Future<sql.Database> db() async {
    return sql.openDatabase(
      'users.db',
      version: 1,
      onCreate: (sql.Database database, int version) async {
        await createTables(database);
      },
    );
  }

  // Create new item (journal)
  static Future<dynamic> createItem(String emp_username, String? emp_password,
      String emp_email, String emp_phone) async {
    final db = await SQLHelper.db();

    final data = {
      'emp_username': emp_username,
      'emp_password': emp_password,
      'emp_email': emp_email,
      'emp_phone': emp_phone
    };
    final id = await db.insert('emp_users', data,
        conflictAlgorithm: sql.ConflictAlgorithm.replace);
    return id;
  }

  // Read all items (journals)
  static Future<List<Map<String, dynamic>>> getItems() async {
    final db = await SQLHelper.db();
    return db.query('emp_users', orderBy: "id");
  }

  // Read a single item by id
  // The app doesn't use this method but I put here in case you want to see it
  static Future<List<Map<String, dynamic>>> getItem(
      String username, String password) async {
    final db = await SQLHelper.db();

    return db.query('emp_users',
        where: "emp_username = ? AND emp_password=? ",
        whereArgs: [username, password],
        limit: 1);
  }

  // Update an item by id
  static Future<int> updateItem(
      int id, String title, String? descrption) async {
    final db = await SQLHelper.db();

    final data = {
      'title': title,
      'description': descrption,
      'createdAt': DateTime.now().toString()
    };

    final result =
        await db.update('items', data, where: "id = ?", whereArgs: [id]);
    return result;
  }

  // Delete
  static Future<void> deleteItem(int id) async {
    final db = await SQLHelper.db();
    try {
      await db.delete("items", where: "id = ?", whereArgs: [id]);
    } catch (err) {
      debugPrint("Something went wrong when deleting an item: $err");
    }
  }
}
